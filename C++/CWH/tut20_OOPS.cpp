//Object oriented programming in C++

// Why OOPS? (Object Oriented Programming Style) 
//As the size of the program increases, the readability, bug free nature and maintainability of the program decreases.
//Languages like C relied upon Functions or Procedures.
//data security was easily compromised.
//Using classes solves this problem by using a *real world scenario*.

//****Procedure oriented programming****
//Set of instructions for the computer to follow.
//Main focus is on functions and not on flow of data.
//Functions can use local or global data.
//Data moves openly from function to function.(Main focus here is on functions.)

//****Object oriented programming****
//Works on the concept of classes and objects.
//***A class is a template to create objects.***
// Example of a class: Class Animal, Sound,run,action
// Object dog,bark running true, sit down
//Treats data as a critical element.
// Decomposes the problem in objects and builds data and functions around the objects.

// BASIC CONCEPTS AND TERMINOLOGIES IN OBJECT ORIENTED PROGRAMMING.
// CLASS- Basic template for creating objects.
//Objects- Basic runtime entities created using the class template.
// Data Abstraction & Encapsulation: Wrapping data and functions into a single unit called class.
// Abstraction: Separation of concerns. Layers of Abstraction: Simplifying the concepts untill matter gets simple as new.
//Inheritance: Properties of one class can be inherited into others.(Excellent example of code reusability.Insane Increase in readability of the program.)
//Polymorphism: Ability to take different forms.(multiple forms) Example: Function Overloading.
//Dynamic binding: Code which will execute is not known untill the program runs.Good Feature of Object Oriented Programming.
//Message Passing: Object.message(information) call Format.

//BENEFITS OF OBJECT ORIENTED PROGRAMMING.
//Better Code reusability using objects and inheritance.
//Principle of data hiding helps build secure systems.
//Multiple objects can coexist without any interference.
//Software complexity can be easily managed. 

